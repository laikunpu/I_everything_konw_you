package com.smith.http.webservice.entity;

public class Bean_common_detail_download {
	private String downloadText;
	private int downloadtype;
	private String downloadUrl;
	private String downloadUrlMsg;

	public String getDownloadText() {
		return downloadText;
	}

	public void setDownloadText(String downloadText) {
		this.downloadText = downloadText;
	}

	public int getDownloadtype() {
		return downloadtype;
	}

	public void setDownloadtype(int downloadtype) {
		this.downloadtype = downloadtype;
	}

	public String getDownloadUrl() {
		return downloadUrl;
	}

	public void setDownloadUrl(String downloadUrl) {
		this.downloadUrl = downloadUrl;
	}

	public String getDownloadUrlMsg() {
		return downloadUrlMsg;
	}

	public void setDownloadUrlMsg(String downloadUrlMsg) {
		this.downloadUrlMsg = downloadUrlMsg;
	}

}
